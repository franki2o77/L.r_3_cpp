#include <iostream>

int main() {
    int m, k, n, x, sum = 0;

    // Введення значень m, x, n, k
    std::cout << "Введіть значення m: ";
    std::cin >> m;

    std::cout << "Введіть значення n: ";
    std::cin >> n;

    std::cout << "Введіть значення k: ";
    std::cin >> k;

    std::cout << "Введіть початкове значення x: ";
    std::cin >> x;

    // Цикл для обчислення суми, поки вона менша або дорівнює k
    while (sum <= k) {
        int term = (x - 1) * (1 - 3 * m) * n;  // Обчислення терміну
        sum += term;  // Додавання терміну до суми
        std::cout << "Поточна сума: " << sum << std::endl;

        // Якщо сума перевищує k, виходимо з циклу
        if (sum > k) {
            break;
        }

        ++x;  // Збільшуємо x на 1 для наступної ітерації
    }

    // Виведення остаточної суми
    std::cout << "Остаточна сума: " << sum << std::endl;

    return 0;
}
// // Якщо користувач введе:
// m = 2
// n = 3
// k = 100
// x = 1
